void main() {
  double age = 30;
  print("age is $age");

  //compound operator

  //addition and assignment operator

  age += 1; //age = age + 1
  print("25 + 1: " + age.toString());

  //subtraction and assignment operator

  age -= 2; // age - 2
  print("26 - 2: " + age.toString());

  //multiplication and assignment operator

  age *= 5; // age * 5
  print("")
}
